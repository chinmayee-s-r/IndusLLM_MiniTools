def langid(text: str):
    if not text:
        return ("unk", 0.0)
    ft = load_ft()
    labels, probs = ft.predict(text.replace("\n"," "), k=1)
    lab = labels[0].replace("__label__", "")
    return (lab, float(probs[0]))

